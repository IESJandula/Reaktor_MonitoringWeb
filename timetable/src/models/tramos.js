export class Tramo{
    
    /**
     * Constructor que instancia un objeto tramo
     * @param {string} numTr 
     * @param {string} dayNumber 
     * @param {string} startHour 
     * @param {string} endHour 
     */
    constructor(numTr,dayNumber,startHour,endHour)
    {
        this.numTr = numTr,
        this.dayNumber = dayNumber,
        this.startHour = startHour,
        this.endHour = endHour
    }

    
}